#include "libft.h"

size_t	ft_numlen(int n)
{
	size_t	ns;

	if (n == 0)
		return (1);
	ns = 0;
	if (n < 0)
		ns++;
	while (n != 0)
	{
		n = n / 10;
		ns++;
	}
	return (ns);
}
